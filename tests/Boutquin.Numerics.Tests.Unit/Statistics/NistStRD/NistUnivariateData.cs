namespace Boutquin.Numerics.Tests.Unit.Statistics.NistStRD;
/// <summary>
/// NIST Statistical Reference Datasets — univariate summary statistics
/// data and certified mean, standard deviation, and (where published)
/// lag-1 autocorrelation. Transcribed verbatim from
/// <c>https://www.itl.nist.gov/div898/strd/univ/univ.shtml</c>.
/// </summary>
/// <remarks>
/// <para>
/// Each accessor returns the raw observations as <see cref="decimal"/> to match the
/// precision of the <see cref="Boutquin.Numerics.Statistics.WelfordMoments"/>
/// accumulator, together with NIST-certified mean, variance, and standard deviation.
/// Variance is derived from the NIST-published standard deviation as
/// <c>s² = StdDev · StdDev</c> because the NIST univariate suite publishes <c>s</c>
/// directly but not <c>s²</c>.
/// </para>
/// <para>
/// Data values are spelled as <see cref="decimal"/> literals (no implicit
/// <c>double → decimal</c> conversion) so that pathological datasets such as
/// <c>NumAcc4</c> retain the full published precision. Certified statistics are
/// stored as <see cref="double"/> because the NIST bar is 14 significant digits,
/// comfortably inside <c>double</c> precision.
/// </para>
/// </remarks>
